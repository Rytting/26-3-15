void TFT_ShowChinese(uint16_t x,uint16_t y,char *s,uint16_t color)
{
    while(*s)
    {
        for(int i=0;i<sizeof(ChineseFont)/sizeof(ChineseFont[0]);i++)
        {
            if(ChineseFont[i].index[0]==*s)
            {
                for(int j=0;j<16;j++)
                {
                    for(int k=0;k<16;k++)
                    {
                        if(ChineseFont[i].code[j*2 + k/8] & (0x80>>(k%8)))
                            TFT_DrawPoint(x+k,y+j,color);
                    }
                }
                x+=16;
            }
        }
        s++;
    }
}