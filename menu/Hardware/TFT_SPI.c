void TFT_DrawPoint(uint16_t x, uint16_t y, uint16_t color)
{
    if(x >= TFT_WIDTH || y >= TFT_HEIGHT) return;

    TFT_SetWindow(x, y, 1, 1);
    CS_LOW();
    DC_HIGH();
    SPI_Write(color >> 8);
    SPI_Write(color & 0xFF);
    CS_HIGH();
}

void TFT_Clear(uint16_t color)
{
    uint16_t i,j;
    TFT_SetWindow(0,0,320,240);
    CS_LOW();
    DC_HIGH();
    for(i=0;i<320;i++)
    {
        for(j=0;j<240;j++)
        {
            SPI_Write(color>>8);
            SPI_Write(color&0xFF);
        }
    }
    CS_HIGH();
}
// 清指定矩形区域（x,y为起点，w为宽度，h为高度，color为填充色）
void TFT_ClearRect(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color)
{
    uint16_t i, j;

    if(x >= TFT_WIDTH || y >= TFT_HEIGHT) return;
    if((x + w) > TFT_WIDTH || (y + h) > TFT_HEIGHT) return;

    TFT_SetWindow(x, y, w, h);
    CS_LOW();
    DC_HIGH();
    for(i = 0; i < h; i++)
    {
        for(j = 0; j < w; j++)
        {
            SPI_Write(color >> 8);
            SPI_Write(color & 0xFF);
        }
    }
    CS_HIGH();
}

