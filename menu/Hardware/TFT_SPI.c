#define TFT_CS_PIN    GPIO_Pin_0
#define TFT_DC_PIN    GPIO_Pin_1
#define TFT_SCK_PIN   GPIO_Pin_5
#define TFT_MOSI_PIN  GPIO_Pin_7

#define TFT_CTRL_PORT GPIOB
#define TFT_SPI_PORT  GPIOA

#define CS_LOW()  GPIO_ResetBits(TFT_CTRL_PORT, TFT_CS_PIN)
#define CS_HIGH() GPIO_SetBits(TFT_CTRL_PORT, TFT_CS_PIN)
#define DC_LOW()  GPIO_ResetBits(TFT_CTRL_PORT, TFT_DC_PIN)
#define DC_HIGH() GPIO_SetBits(TFT_CTRL_PORT, TFT_DC_PIN)

void SPI_Write(uint8_t dat)
{
    while(SPI_I2S_GetFlagStatus(SPI1, SPI_I2S_FLAG_TXE) == RESET);
    SPI_I2S_SendData(SPI1, dat);
    while(SPI_I2S_GetFlagStatus(SPI1, SPI_I2S_FLAG_BSY) == SET);
}

void TFT_Write_Cmd(uint8_t cmd)
{
    CS_LOW();
    DC_LOW();
    SPI_Write(cmd);
    CS_HIGH();
}

void TFT_Write_Data(uint8_t dat)
{
    CS_LOW();
    DC_HIGH();
    SPI_Write(dat);
    CS_HIGH();
}

void TFT_SetWindow(uint16_t x, uint16_t y, uint16_t w, uint16_t h)
{
    TFT_Write_Cmd(0x2A);
    TFT_Write_Data(x>>8);
    TFT_Write_Data(x&0xFF);
    TFT_Write_Data((x+w-1)>>8);
    TFT_Write_Data((x+w-1)&0xFF);

    TFT_Write_Cmd(0x2B);
    TFT_Write_Data(y>>8);
    TFT_Write_Data(y&0xFF);
    TFT_Write_Data((y+h-1)>>8);
    TFT_Write_Data((y+h-1)&0xFF);

    TFT_Write_Cmd(0x2C);
}

