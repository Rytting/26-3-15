void TFT_FillBlock(uint16_t x, uint16_t y, uint8_t size, uint16_t color)
{
    uint8_t i, j;
    for(i = 0; i < size; i++)
    {
        for(j = 0; j < size; j++)
        {
            TFT_DrawPoint(x + j, y + i, color);
        }
    }
}

void TFT_ShowChar(uint16_t x, uint16_t y, uint8_t ch, uint16_t color)
{
    uint8_t col, row;
    uint8_t temp;
    uint8_t index;
    uint8_t size = 2;   // 字体放大倍数

    if(ch < 0x20 || ch > 0x7E) return;
    index = ch - 0x20;

    for(col = 0; col < 8; col++)
    {
        temp = ASCII_8x8[index][col];
        for(row = 0; row < 8; row++)
        {
            if(temp & (1 << row))
            {
                TFT_FillBlock(x + col * size, y + row * size, size, color);
            }
        }
    }
}

void TFT_ShowString(uint16_t x, uint16_t y, char *str, uint16_t color)
{
    uint16_t cur_x = x;
    uint16_t cur_y = y;
    uint8_t size = 2;   // 和 ShowChar 保持一致

    while(*str)
    {
        if(*str == '\n')
        {
            cur_x = x;
            cur_y += 8 * size + 4;
            str++;
            continue;
        }

        TFT_ShowChar(cur_x, cur_y, *str, color);
        cur_x += 8 * size;

        if(cur_x >= TFT_WIDTH - 8 * size)
        {
            cur_x = x;
            cur_y += 8 * size + 4;
        }

        str++;
    }
}
